package java_random_coding;

public class string_iteration_like_array {
    public static void main(String[] args) {
        String sc="hi my() name is(al) aryan";
        System.out.println(sc);
        System.out.println(sc.charAt(0));
        for (int i = 0; i < sc.length(); i++) {
            System.out.println(sc.charAt(i));
        }
    }
}
